package practice;

public class Lab {
    public static void main(String[] args) {

        int age = 7;
        System.out.println(age);    // 7
        System.out.println(age++);  // 7
        System.out.println(age);    // 8
        System.out.println(++age);  // 9
        System.out.println(age);    // 9
        System.out.println(age--);  // 9
        System.out.println(age);    // 8
        System.out.println(--age);  // 7
        System.out.println(age);    // 7

    }
}
